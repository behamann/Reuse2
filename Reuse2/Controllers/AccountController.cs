        //
        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model, string type)
        {
            ApplicationDbContext db = new ApplicationDbContext();
            ViewBag.tipoDeInstituicaoID = new SelectList(db.Tipos, "tipoDeInstituicaoID", "nome");
            if (model.role == "User" && model.tipoDeInstituicaoID == 0)
                model.tipoDeInstituicaoID = 32;
            if (ModelState.IsValid)
            {
                var user = new ApplicationUser
                {
                    UserName = model.UserName,
                    Email = model.Email,
                    cep = model.cep,
                    bairro = model.bairro,
                    cidade = model.cidade,
                    endereco = model.endereco,
                    estado = model.estado,
                    PhoneNumber = model.PhoneNumber,
                    telefone = model.telefone,
                    areaDeCobertura = model.areaDeCobertura,
                    cnpj = model.cnpj,
                    descricaoDaCausa = model.descricaoDaCausa,
                    itensNecessitados = model.itensNecessitados,
                    metodoDeColeta = model.metodoDeColeta,
                    nomeDoResponsavel = model.nomeDoResponsavel,
                    restricoesDeColeta = model.restricoesDeColeta,
                    tipoDeInstituicao = model.tipoDeInstituicao,
                    role = model.role,
                    tipoDeInstituicaoID = model.tipoDeInstituicaoID,
                };

                if (model.File != null)
                {
                    string pic = System.IO.Path.GetFileName(model.File.FileName);
                    string path = System.IO.Path.Combine(
                                           Server.MapPath("~/Content/img/User"), pic);
                    // file is uploaded
                    model.File.SaveAs(path);
                    user.avatar = model.File.FileName;
                }
                else
                {
                    user.avatar = "profile.png";
                }
                try {
                    var result = await UserManager.CreateAsync(user, model.Password);
                    if (result.Succeeded)
                    {
                        /*await SignInManager.SignInAsync(user, isPersistent:false, rememberBrowser:false);*/

                        // For more information on how to enable account confirmation and password reset please visit http://go.microsoft.com/fwlink/?LinkID=320771
                        // Send an email with this link
                        string code = await UserManager.GenerateEmailConfirmationTokenAsync(user.Id);
                        var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
                        await UserManager.SendEmailAsync(user.Id, "Confirme a sua conta", callbackUrl);

                        return RedirectToAction("DisplayEmail");
                    }

                    AddErrors(result);
                }
                catch (DbEntityValidationException e)
                {
                    foreach (var eve in e.EntityValidationErrors)
                    {
                        foreach (var ve in eve.ValidationErrors)
                        {
                            ModelState.AddModelError(ve.PropertyName, ve.ErrorMessage);
                        }
                    }
                    return View(model);
                }                
            }
            // If we got this far, something failed, redisplay form
            return View(model);
        }

        //
        // GET: /Account/ConfirmEmail
        [AllowAnonymous]
        public async Task<ActionResult> ConfirmEmail(string userId, string code)
        {
            if (userId == null || code == null)
            {
                return View("Error");
            }
            IdentityResult result;
            try
            {
                result = await UserManager.ConfirmEmailAsync(userId, code);
            }
            catch (InvalidOperationException ioe)
            {
                // ConfirmEmailAsync throws when the userId is not found.
                ViewBag.errorMessage = ioe.Message;
                return View("Error");
            }

            if (result.Succeeded)
            {
                return View();
            }

            // If we got this far, something failed.
            AddErrors(result);
            ViewBag.errorMessage = "ConfirmEmail failed";
            return View("Error");
        }

